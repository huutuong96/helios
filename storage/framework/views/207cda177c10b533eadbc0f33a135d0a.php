        var productData = google.visualization.arrayToDataTable(JSON.parse('<?php echo $jsonData_by_category; ?>'));

        // Optional; add a title and set the width and height of the chart
        var productOptions = {
            'title': 'Thống kê sản phẩm theo danh mục',
